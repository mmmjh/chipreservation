package com.fenjian.bean;

public class Chip {

	private String type; //芯片类型
	private int amount;  //芯片数量
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	
}
